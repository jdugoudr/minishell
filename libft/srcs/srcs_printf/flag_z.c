#include "flags.h"

int		init_z(const char c, t_flags *el)
{
	int	nb_read;

	nb_read = 0;
	if (c == 'z')
	{
		el->fc |= Z_FLAG;
		nb_read += 1;
	}
	return (nb_read);
}
