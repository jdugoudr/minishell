#include "flags.h"

int		init_j(const char c, t_flags *el)
{
	int	nb_read;

	nb_read = 0;
	if (c == 'j')
	{
		el->fc |= J_FLAG;
		nb_read += 1;
	}
	return (nb_read);
}
