#include "libft.h"

char	*ft_ctoa(char c)
{
	char	*s;

	if ((s = (char *)malloc(2 * sizeof(char))) == NULL)
		return (NULL);
	s[0] = c;
	s[1] = '\0';
	return (s);
}
