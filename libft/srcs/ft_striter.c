#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	i;
	size_t	l;

	i = 0;
	l = 0;
	if (s == NULL || f == NULL)
		return ;
	l = ft_strlen(s);
	while (i < l)
	{
		(*f)(s + i);
		i += 1;
	}
}
